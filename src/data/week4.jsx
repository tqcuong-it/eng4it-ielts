// Helper: get all vocabulary from all days
export function getAllVocabulary() {
  return week4.days.flatMap((day, dayIndex) =>
    day.vocabulary.map((word, wordIndex) => ({
      ...word,
      id: `w4-d${dayIndex + 1}-${wordIndex}`,
      dayId: day.id,
      dayTitle: day.title,
    }))
  )
}
